-- 테이블 삭제
DROP TABLE IF EXISTS MSS_PRODUCT CASCADE;
DROP TABLE IF EXISTS MSS_BRAND CASCADE;
DROP TABLE IF EXISTS MSS_CATEGORY CASCADE;

-- MSS_BRAND 테이블 생성
CREATE TABLE MSS_BRAND
(
    BRAND_ID   BIGINT GENERATED BY DEFAULT AS IDENTITY,
    BRAND_NAME VARCHAR(200) NOT NULL UNIQUE,
    REG_USER   VARCHAR(50)  NOT NULL COMMENT '등록자',
    UPD_USER   VARCHAR(50)  NOT NULL COMMENT '수정자',
    REG_DATE   DATETIME     NOT NULL COMMENT '등록일시',
    UPD_DATE   DATETIME     NOT NULL COMMENT '수정일시',
    PRIMARY KEY (BRAND_ID)
);

-- MSS_CATEGORY 테이블 생성
CREATE TABLE MSS_CATEGORY
(
    CATEGORY_ID   BIGINT GENERATED BY DEFAULT AS IDENTITY,
    CATEGORY_TYPE VARCHAR(50)  NOT NULL UNIQUE,
    CATEGORY_NAME VARCHAR(255) NOT NULL UNIQUE,
    REG_USER      VARCHAR(50)  NOT NULL COMMENT '등록자',
    UPD_USER      VARCHAR(50)  NOT NULL COMMENT '수정자',
    REG_DATE      DATETIME     NOT NULL COMMENT '등록일시',
    UPD_DATE      DATETIME     NOT NULL COMMENT '수정일시',
    PRIMARY KEY (CATEGORY_ID)
);

-- MSS_PRODUCT 테이블 생성
CREATE TABLE MSS_PRODUCT
(
    PRODUCT_ID    BIGINT GENERATED BY DEFAULT AS IDENTITY,
    STATUS_TYPE   VARCHAR(10)  NOT NULL,
    PRODUCT_NAME  VARCHAR(200) NOT NULL,
    BRAND_ID      BIGINT       NOT NULL,
    CATEGORY_TYPE VARCHAR(50)  NOT NULL,
    PRICE         BIGINT,
    REG_USER      VARCHAR(50)  NOT NULL COMMENT '등록자',
    UPD_USER      VARCHAR(50)  NOT NULL COMMENT '수정자',
    REG_DATE      DATETIME     NOT NULL COMMENT '등록일시',
    UPD_DATE      DATETIME     NOT NULL COMMENT '수정일시',
    PRIMARY KEY (PRODUCT_ID)
);
